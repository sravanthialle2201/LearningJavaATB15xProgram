package ex_01_Java_Basics;

public class Lab014_Char_Literals {
    public static void main(String[] args) {
        char c1='A';
        System.out.println(c1);
        //characters can be A-Z,a-z,!@#$%^&*()
        char c2='@';
        char c3='$';
        char c4=')';
        char c5=' ';//space is also a character
        char c6='9';
        System.out.println(c5);
        System.out.println(c4);

        //Escape Sequence
        char new_line='\n';
        char tab_line='\t';
        char back_space='\b';
        char carriage_return='\r';



    }
}
