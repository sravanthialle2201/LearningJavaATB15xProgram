package Java_Operators;

public class Lab_019_Relational_Operators {
    public static void main(String[] args) {
        int a=10,b=20;

        //<---less than
        //<=----less than or equal to
        //>---greater than
        //>=----greater than or equal to
        //==----equal too
        //!=----not equal to

        System.out.println(a>b);
        System.out.println(a==b);
        System.out.println(a<b);
        System.out.println(a!=b);
        System.out.println(a<=b);
        System.out.println(a>=b);
    }
}
